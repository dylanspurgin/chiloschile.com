            <h3>Dr. Fabian Garcia</h3>
            <p>The Patron of New Mexico Chile.</p>
            <p>Dr. Garcia developed "New Mexico No. 9" and released it in 1913,
                (Garcia 1921). New Mexico No. 9 is a cross breed of Mexican
                pesilla and chile pepper Colorado. This was important
                historically because it was the first chile cultivar released
                from New Mexico State University, and it introduced a new pod
                type, "New Mexico", to the world. Pungency of "New Mexico No. 9"
                is estimated to have been around 1,000-1,500 Scoville Heat Units.</p>
            <p>Today the New Mexico pod type is also called Long Green and is
                now a 400+ million dollar industry in New Mexico.</p>
            <p>New Mexico State University has the longest continuous program of
                chile improvement in the world. All "New Mexican" type chiles
                grown today gained their genetic base from cultivars developed
                at NMSU.</p>
            <p>Born in 1871 in Chihuahua, Mexico. Died August 6, 1948 in New
                Mexico. Dr. Garcia's grandmother brought him to New Mexico as a
                child. Dr. Garcia was known to be astute with finances and had
                the reputation of a gentleman. He willed his estate to NMSU with
                a directve that the funds where to be used to educate poor
                youths, as he knew their plight.</p>

